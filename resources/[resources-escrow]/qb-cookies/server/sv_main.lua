RegisterServerEvent('qb-cookies:server:add:to:register')
AddEventHandler('qb-cookies:server:add:to:register', function(Price, Note)
    local RandomID = math.random(1111,9999)
    Config.ActivePayments[RandomID] = {['Price'] = Price, ['Note'] = Note}
    TriggerClientEvent('qb-cookies:client:sync:register', -1, Config.ActivePayments)
end)

RegisterServerEvent('qb-cookies:server:pay:receipt')
AddEventHandler('qb-cookies:server:pay:receipt', function(Price, Note, Id)
    local src = source
    local Player = Framework:GetPlayer(src)
    if Player.Functions.RemoveMoney('cash', Price, 'cookies') then
        if Config.ActivePayments[tonumber(Id)] ~= nil then
            Config.ActivePayments[tonumber(Id)] = nil
            
            TriggerEvent('qb-cookies:give:receipt:to:workers', Note, Price)
            TriggerClientEvent('qb-cookies:client:sync:register', -1, Config.ActivePayments)
            TriggerEvent("qb-bossmenu:server:addAccountMoney", "cookies", Price)

            TriggerClientEvent('QBCore:Notify', src, 'You paid $'..Price..'.' , "success")
        else
            TriggerClientEvent('QBCore:Notify', src, 'Error..', "error")
        end
    else
        TriggerClientEvent('QBCore:Notify', src, 'You don\'t have enough cash.', "error")
    end
end)

